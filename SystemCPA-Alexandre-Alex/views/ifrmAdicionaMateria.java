    private void cadCadastrarMateriaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadCadastrarMateriaActionPerformed
        Materia auxMateria = new Materia();
        auxMateria.setId(Integer.parseInt(txtCodMateria.getText()));
        auxMateria.setNome(txtNomeMateria.getText());
        auxMateria.setQtdAula(txtQtdAula.getText());
        listMateria MA = new listMateria();
        MA.inserir(auxMateria);
        
        JOptionPane.showMessageDialog(
        
                this,
                "Código da Matéria: "+this.txtCodMateria.getText()+"\n"+
                "Nome: "+this.txtNomeMateria.getText()+"\n"+
                "Quantidade de Aula no Período: "+this.txtQtdAula.getText()+"\n"
        );
        this.dispose();
    }//GEN-LAST:event_cadCadastrarMateriaActionPerformed

