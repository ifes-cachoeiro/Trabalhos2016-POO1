    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        this.dispose();
        int i = 0;
        int codAux = Integer.parseInt(tfCodEspecialidade.getText());
        for (Especialidade e : DataBase.lstEspecialidade) {
            if (e.getCod_especialidade() == codAux) {
                JFrmExcluirEspecialidadeConfirma eec = new JFrmExcluirEspecialidadeConfirma(e);
                eec.setVisible(true);
                i = 1;
                break;
            }
        }
        if (i == 0) {
            String text = "ERRO! Especialidade não encontrada!!!";
            JFrmErroExcluirEspecialidade s = new JFrmErroExcluirEspecialidade(text);
            s.setVisible(true);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

