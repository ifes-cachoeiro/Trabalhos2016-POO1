    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int cod, i = 0;
        if (tfCod.getText().equals("")) {

        } else {
            TipoTurmaCRUD ec = new TipoTurmaCRUD();
            DefaultTableModel tabela = (DefaultTableModel) tabEspecialidade.getModel();
            tabela.setNumRows(0);
            cod = Integer.parseInt(tfCod.getText());
            Tipo_turma t = new Tipo_turma();
            if (ec.ler(cod) == null) {
                i = 1;
                tfCod.setText("");
            }
            if (i != 1) {
                tabela.addRow(t.obterDados(ec.ler(cod)));
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

