    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (tfNomeAddTipoTurma.getText().equals("")
                || tfPrecoAddTipoTurma.getText().equals("")
                || tfDescricaoAddTipoTurma.getText().equals("")) {
        } else {
            Tipo_turma tpp = new Tipo_turma();
            tpp.setNome_tipo(tfNomeAddTipoTurma.getText());
            tpp.setPreco(Double.parseDouble(tfPrecoAddTipoTurma.getText()));
            tpp.setDescricao(tfDescricaoAddTipoTurma.getText());

            TipoTurmaCRUD tpc = new TipoTurmaCRUD();

            if (jj == 0) {
                text = "Tipo Turma cadastrado com sucesso!!!";
                tpc.inserir(tpp);
            } else if (jj == 1) {
                text = "Tipo Turma alterado com sucesso!!!";
                tpp.setCod_tipo_turma(codC);
                tpc.alterar(codC, tpp);
            }
            this.dispose();
            JFrmSucesso fs = new JFrmSucesso(text);
            fs.setVisible(true);

        }
    }//GEN-LAST:event_jButton1ActionPerformed

