    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int i = 0;
        if (tfCodExcluirTipoTurma.getText().equals("")) {

        } else {
            this.dispose();
            int codAux = Integer.parseInt(tfCodExcluirTipoTurma.getText());
            for (Tipo_turma tp : DataBase.lstTipoTurma) {
                if (tp.getCod_tipo_turma() == codAux) {
                    JFrmExcluirTipoTurmaConfirma etp = new JFrmExcluirTipoTurmaConfirma(tp);
                    etp.setVisible(true);
                    i = 1;
                    break;
                }
            }
            if (i == 0) {
                String text = "ERRO! Tipo de Turma não encontrado!!!";
                JFrmErroExcluirTipoTurma etp = new JFrmErroExcluirTipoTurma(text);
                etp.setVisible(true);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

