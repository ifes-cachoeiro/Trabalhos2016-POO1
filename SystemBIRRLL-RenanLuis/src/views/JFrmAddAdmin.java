    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int jjj = 0;
        String text = "";
        if (tfNomeAdmin.getText().equals("")
                || tfEmailAdmin.getText().equals("")
                || tfSenhaAdmin.getText().equals("")) {
        } else {
            Admin tpp = new Admin();
            tpp.setSenha(tfSenhaAdmin.getText());
            if(tpp.getSenha().length() == 6 && tpp.ehInteiro(tpp.getSenha()) < 6 && tpp.ehInteiro(tpp.getSenha()) > 1){
                jjj=1;
            }
            if (tpp.validEmail(tfEmailAdmin.getText()) == true && jjj==1) {
                tpp.setEmail(tfEmailAdmin.getText());
                tpp.setNome(tfNomeAdmin.getText());
                tpp.setSenha(tfSenhaAdmin.getText());
                AdminCRUD tpc = new AdminCRUD();
                tpc.inserir(tpp);
                this.dispose();
                JFrmSucesso fs = new JFrmSucesso("Administrador cadastrado com sucesso!!!", 1);
                fs.setVisible(true);
            } else {
                this.dispose();
                text +="ERRO! Email ou Senha inv√°lido";
                JFrmSucesso m = new JFrmSucesso(text, 2);
                m.setVisible(true);
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

