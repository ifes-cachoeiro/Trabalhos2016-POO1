    private void jBbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBbuscarActionPerformed
        UsuarioCRUD userCRUD;
        Usuario user;

        if (!jTbusca.getText().isEmpty()) {

            user = new Usuario();

            userCRUD = new UsuarioCRUD();
            int idTemporario = Integer.parseInt(jTbusca.getText());

            try {
                user.setId(idTemporario);

                ArrayList<Usuario> lista = new ArrayList<>();
                lista = userCRUD.ler();
                int foi = 0;
                for (Usuario u : lista) {
                    //if(user == userCRUD.ler(idTemporario)){  
                    if (u.getId() == user.getId()) {

                        TfNome.setText("" + u.getNome());
                        tFuserName.setText("" + u.getUserName());
                        TfEmail.setText("" + u.getEmail());
                        tFidUser.setText("" + u.getId());
                        PfsenhaNova.setText("" + u.getPass());
                        PfConfirmarNova.setText("" + u.getPass());
                        jPdados.setVisible(true);
                        foi = 1;

                    } else {

                    }

                }

                if (foi == 0) {
                    JOptionPane.showMessageDialog(this, "Usuario não foi encontrado!");
                }
            } catch (NumberFormatException nfe) {
                tFidUser.setText("");
                JOptionPane.showMessageDialog(this, "Informe um código válido!\nERROR - " + nfe.getMessage());

            }

        } else {
            tFidUser.setText("");
            JOptionPane.showMessageDialog(this, "Informe um código válido!");
        }

    }//GEN-LAST:event_jBbuscarActionPerformed

    private void jBalterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBalterarActionPerformed
        Usuario user = new Usuario();
        UsuarioCRUD userCRUD = new UsuarioCRUD();
        int idTemporario = Integer.parseInt(jTbusca.getText());

        String txtEmail;
        if (tFuserName.getText().length() > 0) {

            if (TfEmail.getText().length() > 0) {

                if (PfsenhaNova.getText().length() > 0) {

                    if (PfsenhaNova.getText().equals(PfConfirmarNova.getText())) {
                        ArrayList<Usuario> lista = new ArrayList<>();
                        lista = userCRUD.ler();

                        for (Usuario u : lista) {
                            user.setId(idTemporario);
                            if (u.getId() == user.getId()) {

                                txtEmail = TfEmail.getText();
                                u.setEmail(this.ValidaEmail(txtEmail));
                                u.setUserName(this.tFuserName.getText());
                                u.setPass(this.PfsenhaNova.getText());
                                u.setNome(this.TfNome.getText());

                                if (u.getEmail() != null) {
                                    JOptionPane.showMessageDialog(this, "Usuario atualizado com sucesso!");

                                    tFuserName.setText("");
                                    TfEmail.setText("");
                                    PfConfirmarNova.setText("");
                                    PfsenhaNova.setText("");
                                    TfNome.setText("");

                                    this.dispose();

                                } else {
                                    JOptionPane.showMessageDialog(null, "E-mail inválido!!");
                                }

                            }

                        }
                    } else {
                        JOptionPane.showMessageDialog(this, "As senhas não conferem!");
                    }

                } else {
                    JOptionPane.showMessageDialog(this, "O campo senha não pode ficar em branco!");
                }
            } else {

                JOptionPane.showMessageDialog(this, "Informe um E-mail por favor!");
            }
        } else {

            JOptionPane.showMessageDialog(this, "Informe um nome por favor!");
        }
    }//GEN-LAST:event_jBalterarActionPerformed

