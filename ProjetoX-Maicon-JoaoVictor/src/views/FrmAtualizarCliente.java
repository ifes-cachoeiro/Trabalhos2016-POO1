    private void jBalterar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBalterar1ActionPerformed

        Cliente cliente = new Cliente();
        ClienteCRUD clienteCRUD = new ClienteCRUD();
        ArrayList<Cliente> lista = new ArrayList<>();
        int idTemporario = Integer.parseInt(jTbusca.getText());

        if (TfCliente.getText().length() > 0) {

            if (TfNome1.getText().length() > 0) {

                if (TfTelefone.getText().length() > 0) {

                    if (TfEndereco.getText().length() > 0) {
                        lista = clienteCRUD.ler();

                        for (Cliente c : lista) {
                            cliente.setId(idTemporario);
                            if (c.getId() == cliente.getId()) {

                                c.setNome(this.TfNome1.getText());
                                c.setTelefone(this.TfTelefone.getText());
                                c.setEndereco(this.TfEndereco.getText());

                                if (c.getNome() != null) {
                                    JOptionPane.showMessageDialog(this, "Cliente atualizado com sucesso!");

                                    TfCliente.setText("");
                                    TfNome1.setText("");
                                    TfTelefone.setText("");
                                    TfEndereco.setText("");

                                    this.dispose();

                                }
                            } else {

                                JOptionPane.showMessageDialog(this, "Informe um nome por favor!");
                            }
                        }
                    }
                }
            }
        }
    }//GEN-LAST:event_jBalterar1ActionPerformed

