    private void btBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBuscarActionPerformed
        ClienteCRUD clienteCrud = new ClienteCRUD();
        
        while(true){
            String cpfAux = tfCpf.getText();
            if(Util.isEmptyString(cpfAux)){
                String aux = "";
                for(Cliente c: clienteCrud.ler()){
                    aux += "######################################### \n";
                    aux += ("Id do Cliente: "+c.getId()+"\n");
                    aux += ("Nome Completo: "+c.getNomeCompleto()+"\n");
                    aux += ("CPF: "+c.getCpf()+"\n");
                    aux += ("Sexo: "+c.getSexo()+"\n");
                    aux += ("Endereço: "+c.getEndereco()+"\n");
                    aux += ("Telefone: "+c.getTelefone()+"\n");
                }
                taListarClientes.setText(aux);
                break;
            }else{
                //String aux = ttfCpfgetText();
                for(Cliente c: lstClientes){
                    
                    if(c.getCpf().contains(cpfAux)){
                        taListarClientes.setText("Nome Completo: " 
                            +c.getNomeCompleto() +"\nCPF: "
                            +c.getCpf()+"\nSexo: "+c.getSexo()
                            + "\nEndereço: " +c.getEndereco()+ "\nTelefone: "
                            +c.getTelefone());
                    }
                }
                break;
            }
        }
    }//GEN-LAST:event_btBuscarActionPerformed


