    private void btBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBuscarActionPerformed
        VendaCRUD vendaCrud = new VendaCRUD();
                
        while(true){
            Integer idAux = Integer.parseInt(tfID.getText());
            String aux = "";
            
            for(Venda v: lstVendas){
                if(v.getId() == idAux){
                    aux += "######################\n";
                    aux += "Informações da Venda: \n";
                    aux += "ID da Venda: "+v.getId()+"\n";
                    aux += "Nome do Cliente: "+v.cliente.getNomeCompleto()+"\n";
                    aux += "Fechada: "+v.fechada+"\n";
                    for(Produto p: v.lstProdutos){
                        aux += "------------------------------\n";
                        aux += "ID do Produto: "+p.getId()+"\n";
                        aux += "Nome do Produto: "+p.getNomeProduto()+"\n";
                        aux += "Valor do Produto R$: "+p.getValor()+"\n";
                        aux += "Unidades: "+p.qtd+"\n";
                    }
                    aux += "#######################\n"; 
                    aux += "Valor Total R$: "+v.ValorTotal()+"\n";
                    aux += "\n";
                    aux += "Para Faturar a Venda \n";
                    aux += "Clique em CONFIRMAR. \n";    
                }   
            }
            taListarVenda.setText(aux);
            break;
        }
    }//GEN-LAST:event_btBuscarActionPerformed

