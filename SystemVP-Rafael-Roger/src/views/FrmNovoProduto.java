    private void btCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCadastrarActionPerformed
        Produto novoProduto = new Produto();
        
        
        novoProduto.setNomeProduto(this.tfNome.getText());
        novoProduto.setDescricao(this.tfDescricao.getText());
        try {
            novoProduto.setValor(Double.parseDouble(this.tfValor.getText()));
            if(novoProduto.getValor() > 0){
                Produto.nextID++;
                novoProduto.setId(Produto.nextID);
                ProdutoCRUD produtoCrud = new ProdutoCRUD();
                produtoCrud.inserir(novoProduto);
                this.dispose();
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "formato inválido");
            this.tfValor.setText(null);
            this.tfValor.requestFocusInWindow();
            
        }catch (Exception ex){
            JOptionPane.showMessageDialog(null, "não é permitido valores negativos");
            this.tfValor.setText(null);
            this.tfValor.requestFocusInWindow();
        }   
    }//GEN-LAST:event_btCadastrarActionPerformed


