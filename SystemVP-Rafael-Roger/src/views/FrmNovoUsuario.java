    private void btCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCadastrarActionPerformed
        // TODO add your handling code here:
        Usuario novoUsuario = new Usuario();
        
        novoUsuario.setNomeCompleto(this.tfNome.getText());
        novoUsuario.setEmail(this.tfEmail.getText());
        novoUsuario.setSenha(this.pfSenha.getText());
       
        novoUsuario.setEmail(novoUsuario.validarEmail(this.tfEmail.getText()));    
         
        if(novoUsuario.getEmail() != null){
           Usuario.nextId++;
           novoUsuario.setId(Usuario.nextId);
           UsuarioCRUD userCrud = new UsuarioCRUD();
           userCrud.inserir(novoUsuario);
           this.dispose();
        }
        
    }//GEN-LAST:event_btCadastrarActionPerformed

