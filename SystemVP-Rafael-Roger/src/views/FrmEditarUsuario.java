    private void btBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBuscarActionPerformed
        String nomeAux = tfNomeUsuario.getText();
        
        for(Usuario user: lstUsuarios){
            if(user.getNomeCompleto().contains(nomeAux)){
                Lnome.setText(user.getNomeCompleto());
                tfEmail.setText(user.getEmail());
                pfSenha.setText(user.getSenha());
                
            }
        }
    }//GEN-LAST:event_btBuscarActionPerformed

    private void btAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAlterarActionPerformed
        try {
            Usuario usuario = new Usuario();
            UsuarioCRUD userCrud = new UsuarioCRUD();
            
            String nomeAux = tfNomeUsuario.getText();
            
            for(Usuario u: lstUsuarios){
                if(u.getNomeCompleto().contains(nomeAux)){
                    usuario.setEmail(this.tfEmail.getText());
                    usuario.setSenha(this.pfSenha.getText());
                    
                }
            }
            
            userCrud.alterar(nomeAux, usuario);
            JOptionPane.showMessageDialog(null, "Altera칞칚o efetuada com sucesso.");
            this.dispose();
        } catch (Exception ex) {
            
        }
        
        
    }//GEN-LAST:event_btAlterarActionPerformed


