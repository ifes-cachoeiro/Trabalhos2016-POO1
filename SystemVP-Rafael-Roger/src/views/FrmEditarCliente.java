    private void btBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBuscarActionPerformed
        
        String cpfAux = tfBuscarCliente.getText();
        for(Cliente c: lstClientes){
            if(c.getCpf().contains(cpfAux)){
                Lnome.setText(c.getNomeCompleto());
                Lcpf.setText(c.getCpf());
                Lsexo.setText(c.getSexo());
                tfEndereco.setText(c.getEndereco());
                tfTelefone.setText(c.getTelefone());             
            }  
        } 
    }//GEN-LAST:event_btBuscarActionPerformed

    private void btAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAlterarActionPerformed
        Cliente cliente = new Cliente();
        ClienteCRUD clienteCrud = new ClienteCRUD();
        
        String cpfAux = Lcpf.getText();
        
        for(Cliente c: lstClientes){
            if(c.getCpf().contains(cpfAux)){
                cliente.setEndereco(this.tfEndereco.getText());
                cliente.setTelefone(this.tfTelefone.getText()); 
            }
        }
        clienteCrud.alterar(cpfAux, cliente);
        JOptionPane.showMessageDialog(null, "Alteração efetuada com sucesso.");
        this.dispose();
        
    }//GEN-LAST:event_btAlterarActionPerformed


