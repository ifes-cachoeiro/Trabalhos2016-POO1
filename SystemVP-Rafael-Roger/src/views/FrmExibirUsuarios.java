    private void btBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBuscarActionPerformed
        
        UsuarioCRUD usuarioCrud = new UsuarioCRUD();
        
        while(true){
            String nomeCompletoAux = tfBusca.getText();
            if(Util.isEmptyString(nomeCompletoAux)){
                String aux = "";
                for(Usuario user: usuarioCrud.ler()){
                    aux += "######################################### \n";
                    aux += ("Nome de usuário: "+user.getNomeCompleto()+"\n");
                    aux += ("E-mail de usuário: "+user.getEmail()+"\n");
                }
                taListagemUsuario.setText(aux);
                break;
            }else{
                String aux = tfBusca.getText();
                for(Usuario user: lstUsuarios){
                    
                    if(user.getNomeCompleto().contains(aux)){
                        taListagemUsuario.setText("Nome de usuário: " 
                            +user.getNomeCompleto() +"\n E-mail do usuário: "
                            +user.getEmail());
                    }
                }
                break;
            }
        }
             
    }//GEN-LAST:event_btBuscarActionPerformed


