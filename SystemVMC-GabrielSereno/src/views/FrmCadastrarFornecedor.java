        f.setBairro_fornecedor(tfBairro.getText());
        f.setCEP_fornecedor(tfCEP.getText());
        f.setCidade_fornecedor(tfCidade.getText());
        f.setCnpj_fornecedor(tfCNPJ.getText());
        f.setEmail_fornecedor(tfEmail.getText());
        f.setNome_fornecedor(tfNome.getText());
        f.setEndereco_fornecedor(tfEndereco.getText());
        f.setNumero_fornecedor(tfNumero.getText());
        f.setTelefone1_fornecedor(tfTelefone1.getText());
        f.setTelefone2_fornecedor(tfTelefone2.getText());
        f.setUf_fornecedor(tfUF.getText());
        FornecedorCRUD.cadastrar(f);
        this.dispose();
