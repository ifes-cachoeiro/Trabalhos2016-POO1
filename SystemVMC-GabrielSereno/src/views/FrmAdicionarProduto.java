    private void btAdicionarProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAdicionarProdutoActionPerformed
        int qtd, aux;
        produto = ((ProdutoTableModel)tProduto.getModel()).getProduto(tProduto.getSelectedRow());
        qtd = Integer.parseInt(JOptionPane.showInputDialog("Digite a quantidade:    " + "Só é possivel"
                + " até: " + produto.getEstoque_produto()));
        ProdutoCRUD.excluir(produto);
        aux =   produto.getEstoque_produto() - qtd;
        try {
            produto.setEstoque_produto(aux);
        } catch (Exception ex) {
            Logger.getLogger(null);
        }
        ProdutoCRUD.cadastrar(produto);
        try {
            produto.setEstoque_produto(qtd);
        } catch (Exception ex) {
            Logger.getLogger(null);
    }//GEN-LAST:event_btAdicionarProdutoActionPerformed
        setProduto(produto);
        ProdutoUsadosCRUD.cadastrar(getProduto());
         
        
        tProdutoAdicionado.setModel(new ProdutoTableModel(ProdutoUsadosCRUD.listar()));
    }
    
