    static Produto p = new Produto();
    
    private void btCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCadastrarActionPerformed
        
        
        try {
            p.setEstoque_produto(Integer.parseInt(tfEstoque.getText()));
        } catch (Exception ex) {
            Logger.getLogger(FrmCadastrarProduto.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        p.setMarca_produto(tfMarca.getText());
       
        try {
            p.setMinestoque_produto(Integer.parseInt(tfMinimoEstoque.getText()));
        } catch (Exception ex) {
            Logger.getLogger(FrmCadastrarProduto.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        p.setModelo_produto(tfModelo.getText());
        p.setNome_produto(tfNomeProduto.getText());
        p.setPccomissao_produto(tfComissao.getText());
        
        try {
            p.setPrecocompra_produto(Double.parseDouble(tfPrecoCompra.getText()));
        } catch (Exception ex) {
            Logger.getLogger(FrmCadastrarProduto.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        try {
            p.setPrecovenda_produto(Double.parseDouble(tfPrecoVenda.getText()));
        } catch (Exception ex) {
            Logger.getLogger(FrmCadastrarProduto.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        p.setUnidade_produto(tfUnidade.getText());
        ProdutoCRUD.cadastrar(p);
        this.dispose();
    }//GEN-LAST:event_btCadastrarActionPerformed

