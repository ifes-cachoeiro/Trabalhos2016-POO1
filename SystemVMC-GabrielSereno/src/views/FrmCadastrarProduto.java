    private void btAdicionarFornecedorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAdicionarFornecedorActionPerformed
        FrmAdicionarFornecedor obj = new FrmAdicionarFornecedor(new JFrame(), true);
        obj.setVisible(true);
        if(obj.isConfirmado()){
            p.setFornecedor_produto(obj.getFornecedor());
        
        tfFornecedor.setText(p.getFornecedor_produto().getNome_fornecedor());
        }
    }//GEN-LAST:event_btAdicionarFornecedorActionPerformed

