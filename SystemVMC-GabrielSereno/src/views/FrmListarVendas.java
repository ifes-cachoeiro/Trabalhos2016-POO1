    private void btAlterarVendaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAlterarVendaActionPerformed
        Venda venda = new Venda();
        venda = ((VendaTableModel)tVenda.getModel()).getVenda(tVenda.getSelectedRow());
        FrmAlterarVenda attVenda = new FrmAlterarVenda(venda);
        attVenda.setVisible(true);
    }//GEN-LAST:event_btAlterarVendaActionPerformed

