    private void btProcurarVendasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btProcurarVendasActionPerformed
        Venda venda = new Venda();
        int cod = Integer.parseInt(tfPesquisa.getText());
        venda = VendaCRUD.listar(cod);
        ArrayList <Venda> aux = new ArrayList<>();
        aux.add(venda);
        tVenda.setModel(new VendaTableModel(aux));
    }//GEN-LAST:event_btProcurarVendasActionPerformed

