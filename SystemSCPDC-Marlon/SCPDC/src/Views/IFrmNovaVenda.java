    private void btCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCadastrarActionPerformed
        CrudVenda crudVenda = new CrudVenda();
        CrudCliente crudCliente = new CrudCliente();
        CrudFuncionario crudFuncionario = new CrudFuncionario();
        CrudProduto crudProduto = new CrudProduto();
        Cliente c = crudCliente.pesquisarCliente(Integer.parseInt(tfIdClienteVenda.getText()));
        Funcionario f = crudFuncionario.pesquisarFuncionario(Integer.parseInt(tfIdFuncionarioVenda.getText()));
        Produto p = crudProduto.pesquisarProduto(Integer.parseInt(tfIdProdutoVenda.getText())); 
        crudVenda.NovaVenda(tfDataVenda.getText(), c, f, tfPrevisaoEntregaVenda.getText(), 
                tfRotaVenda.getText(), 
                p, Integer.parseInt(tfQuantidadeVenda.getText()));
        JOptionPane.showMessageDialog(null, "Venda Cadastrada!");
        this.dispose();
    }//GEN-LAST:event_btCadastrarActionPerformed


