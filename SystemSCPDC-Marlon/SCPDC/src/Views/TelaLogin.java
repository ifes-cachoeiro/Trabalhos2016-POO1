    private void btEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEntrarActionPerformed
        Usuario u = new Usuario();
        if (tfEmail.getText().equals(u.getEmail()) && 
                ((JPasswordField)tfSenha).getText().equals(u.getSenha())) {
            this.dispose();
            MenuPrincipal menu = new MenuPrincipal();
            menu.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(null, "Usu√°rio e/ou senha incorretos!");
            this.tfEmail.setText(null);
            this.tfSenha.setText(null);
            this.tfEmail.requestFocusInWindow();
        }

    }//GEN-LAST:event_btEntrarActionPerformed

