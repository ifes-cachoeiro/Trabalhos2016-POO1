    private void btAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAlterarActionPerformed
        CrudFuncionario crudFuncionario = new CrudFuncionario();
        CrudFuncao crudFuncao = new CrudFuncao();
        Funcao func = crudFuncao.pesquisarFuncao(Integer.parseInt(tfAltFunc.getText()));
        crudFuncionario.alterarFuncionario(f, tfAltNome.getText(), tfAltCPF.getText(),
               tfAltTel.getText(), func,tfAltCidade.getText(), 
               tfAltBaiiro.getText());
        JOptionPane.showMessageDialog(null, "Funcionário Alterado!");
        this.dispose();
    }//GEN-LAST:event_btAlterarActionPerformed

