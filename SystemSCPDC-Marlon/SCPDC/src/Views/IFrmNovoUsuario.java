    private void btCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCadastrarActionPerformed
        CrudUsuario crudUsuario = new CrudUsuario();
        if(crudUsuario.VerificarEmail(tfEmailUsuario.getText())){
            JOptionPane.showMessageDialog(null, "email sendo usado ou inválido");
            this.tfEmailUsuario.setText(null);
            this.tfEmailUsuario.requestFocusInWindow();
        }else{
            crudUsuario.NovoUsuario(tfEmailUsuario.getText(), pfSenhaUsuario.getText(), 
                    tfNomeUsuario.getText());
            JOptionPane.showMessageDialog(null, "Usuario Cadastrado!");
            this.dispose();
        }
    }//GEN-LAST:event_btCadastrarActionPerformed


