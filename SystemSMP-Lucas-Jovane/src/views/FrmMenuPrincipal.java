    private void miNovoVeiculoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_miNovoVeiculoActionPerformed
        // TODO add your handling code here:
        IFrmCadastrarVeiculo cadaVeiculoFrm = new IFrmCadastrarVeiculo(0,
            "Cadastrar", "Cadastro de Veículos");
        int aux = cadaVeiculoFrm.verificaExistClient();
        int aux2 = cadaVeiculoFrm.verificaExistCatg();
        if(aux != 0 && aux2 != 0){
            dpPrincipal.add(cadaVeiculoFrm);
            cadaVeiculoFrm.setVisible(true);
        } else{
            cadaVeiculoFrm.fecha();
        }
    }//GEN-LAST:event_miNovoVeiculoActionPerformed

