    private void btPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btPesquisarActionPerformed
        // TODO add your handling code here:
        String cpf = tfCpf.getText();
        cliente = CRUD.ler(cpf);
        if(cliente == null){
            JOptionPane.showMessageDialog(null, "Não encontrado");
        } else {
            // Constroi a janela conforme a ação(Alterar, visualizar e remover)
            switch(aux){
                case 1:
                    IFrmCadastrarCliente editClienteFrm = new IFrmCadastrarCliente(
                        1, "Salvar", "Alteração de Cliente");
                    FrmMenuPrincipal.dpPrincipal.add(editClienteFrm);
                    editClienteFrm.setCliente(cliente);
                    editClienteFrm.setDesabilitarInputs(1);
                    editClienteFrm.setVisible(true);
                break;
                case 2:
                    IFrmCadastrarCliente visuaClienteFrm = new IFrmCadastrarCliente(
                        2, "Fechar", "Visualizar Cliente");
                    FrmMenuPrincipal.dpPrincipal.add(visuaClienteFrm);
                    visuaClienteFrm.setCliente(cliente);
                    visuaClienteFrm.setDesabilitarInputs(0);
                    visuaClienteFrm.setVisible(true);
                break;
                case 3:
                    IFrmCadastrarCliente removeClienteFrm = new IFrmCadastrarCliente(
                        3, "Remover", "Remover Cliente");
                    FrmMenuPrincipal.dpPrincipal.add(removeClienteFrm);
                    removeClienteFrm.setCliente(cliente);
                    removeClienteFrm.setDesabilitarInputs(0);
                    removeClienteFrm.setVisible(true);
                break;
                default:
                    System.out.println("Fazer nada");
                break;
            }
            
        }
        tfCpf.setText("");
        
    }//GEN-LAST:event_btPesquisarActionPerformed

