    private void btCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCadastrarActionPerformed
        // TODO add your handling code here:
        String nome     = tfNome.getText();
        String usuario  = tfUsuario.getText();
        String cpf      = tfCpf.getText();
        String email    = tfEmail.getText();
        String senha    = new String(pfSenha.getPassword()).trim();
        String tel      = tfTel.getText();
        String rua      = tfRua.getText();
        String uf       = String.valueOf(cbUf.getSelectedItem());
        String cidade   = tfCidade.getText();

        func = new Funcionario(usuario, senha, cpf, nome, rua, tel, email, cidade, uf);
        // Switch case para mudar a ação do botão conforme a ação(Alterar, Visualizar e Remover)
        switch(aux){
            case 0:
                CRUD.inserir(func);
                limparTela();
            break;
            case 1:
                CRUD.alterar(cpf, func);
                limparTela();
            break;
            case 2:
                this.dispose();
            break;
            case 3:
                func = CRUD.ler(cpf);
                CRUD.remover(func);
                limparTela();
            break;
        }
    }//GEN-LAST:event_btCadastrarActionPerformed

