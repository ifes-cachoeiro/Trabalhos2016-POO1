public void limparTela(){
    tfCidade.setText("");
    pfSenha.setText("");
    tfCpf.setText("");
    tfEmail.setText("");
    tfUsuario.setText("");
    tfRua.setText("");
    tfTel.setText("");
    tfNome.setText("");
}

public void setTitulo(String titulo){
    lbTitulo.setText(titulo);
}

public void setFuncionario(Funcionario funcionario){
    tfNome.setText(funcionario.getNome());
    tfCpf.setText(funcionario.getCpf());
    tfUsuario.setText(funcionario.getUsuario());
    pfSenha.setText(funcionario.getSenha());
    tfEmail.setText(funcionario.getEmail());
    tfRua.setText(funcionario.getEndereco());
    tfTel.setText(funcionario.getTelefone());
    cbUf.setSelectedItem(funcionario.getUf());
    tfCidade.setText(funcionario.getBairro());
}

public void setDesabilitarInputs(int opc){
    if(opc == 0){
        tfCpf.setEditable(false);
        tfUsuario.setEditable(false);
        tfEmail.setEditable(false);
        tfRua.setEditable(false);
        tfTel.setEditable(false);
        cbUf.setEditable(false);
        tfCidade.setEditable(false);
        pfSenha.setEditable(false);
        tfNome.setEditable(false);
    } else {
        tfCpf.setEditable(false);
    }
}
