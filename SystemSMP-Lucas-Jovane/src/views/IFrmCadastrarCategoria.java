    private void btCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCadastrarActionPerformed
        // TODO add your handling code here:
        
        String id = tfId.getText();           
        String categoria = tfCategoria.getText();   
        String descricao = tfDescricao.getText(); 
        String valorHora = tfValorHora.getText();
        float valHora = Float.parseFloat(valorHora);
        //int ID;
        //cat = new Categoria(categoria, descricao, valHora);
        // Switch case para mudar a ação do botão conforme a ação(Alterar, Visualizar e Remover)
        switch(aux){
            case 0:
                cat = new Categoria(categoria, descricao, valHora);
                CRUD.inserir(cat);
                //Id++;
                //limparTela();
            break;
            case 1:
                //Integer.parseInt(id)
                cat = new Categoria(valHora, descricao, categoria);
                System.out.println("Id do objeto: "+cat.getId());
                System.out.println("Id do tf: "+id);
                CRUD.alterar(Integer.parseInt(id), cat);
                //limparTela();
            break;
            case 2:
                this.dispose();
            break;
            case 3:
                cat = new Categoria(valHora, descricao, categoria);
                cat = CRUD.ler(Integer.parseInt(id));
                CRUD.remover(cat);
                //limparTela();
            break;
        }
        limparTela();
        if(aux == 0){
            Id++;
            tfId.setText(Integer.toString(Id));
        }
    }//GEN-LAST:event_btCadastrarActionPerformed

