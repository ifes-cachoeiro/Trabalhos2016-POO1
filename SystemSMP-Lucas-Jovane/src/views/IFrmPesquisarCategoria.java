    private void btPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btPesquisarActionPerformed
        // TODO add your handling code here:
        int id = Integer.parseInt(tfId.getText());
        cat = CRUD.ler(id);
        if(cat == null){
            JOptionPane.showMessageDialog(null, "Não encontrado");
        } else {
            // Constroi a janela conforme a ação(Alterar, visualizar e remover)
            switch(aux){
                case 1:
                IFrmCadastrarCategoria editCatgFrm = new IFrmCadastrarCategoria(
                    1, "Salvar", "Alteração de Categoria");
                FrmMenuPrincipal.dpPrincipal.add(editCatgFrm);
                editCatgFrm.setCategoria(cat);
                editCatgFrm.setDesabilitarInputs(1);
                editCatgFrm.setVisible(true);
                break;
                case 2:
                IFrmCadastrarCategoria visuaCatgFrm = new IFrmCadastrarCategoria(
                    2, "Fechar", "Visualizar Categoria");
                FrmMenuPrincipal.dpPrincipal.add(visuaCatgFrm);
                visuaCatgFrm.setCategoria(cat);
                visuaCatgFrm.setDesabilitarInputs(0);
                visuaCatgFrm.setVisible(true);
                break;
                case 3:
                IFrmCadastrarCategoria removeCatgFrm = new IFrmCadastrarCategoria(
                    3, "Remover", "Remover Categoria");
                FrmMenuPrincipal.dpPrincipal.add(removeCatgFrm);
                removeCatgFrm.setCategoria(cat);
                removeCatgFrm.setDesabilitarInputs(0);
                removeCatgFrm.setVisible(true);
                break;
                default:
                System.out.println("Fazer nada");
                break;
            }

        }
        tfId.setText("");

    }//GEN-LAST:event_btPesquisarActionPerformed

