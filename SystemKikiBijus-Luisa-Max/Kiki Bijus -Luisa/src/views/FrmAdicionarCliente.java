    private void listarEstados() {

        ArrayList<Estado> listaS = estadoCRUD.read();
        jcUf.removeAllItems();
        jcUf.addItem("Escolha");
        for (Estado lista1 : listaS) {
            jcUf.addItem(lista1.getSigla());
        }
    }

    private void listarCidades(String sigla) {

        ArrayList<Cidade> listaC = cidadeCRUD.read(sigla);
        DefaultComboBoxModel modelo = new DefaultComboBoxModel();
        jcCidade.setModel(modelo);
        jcCidade.removeAllItems();
        jcCidade.addItem("Escolha");
        for (Cidade lista1 : listaC) {
            modelo.addElement(lista1);
        }
    }

