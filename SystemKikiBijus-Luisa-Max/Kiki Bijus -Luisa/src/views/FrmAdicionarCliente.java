    private void brSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_brSalvarActionPerformed
        // TODO add your handling code here:

        Cliente c = new Cliente();

        if (tfNome.getText().equals("") || jcCidade.getSelectedItem().equals("Escolha") || tfBairro.getText().equals("")
                || tfRua.getText().equals("") || tfNumero.getText().equals("") || jfCpf.getText().equals("")
                || jfTel.getText().equals("") || jfData.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Favor preencher todos os valores");
            return;
        } else {
            Cidade cidade = (Cidade) jcCidade.getSelectedItem();
            c.setNome(tfNome.getText());
            c.setCidade(cidade.getCod_cidade());
            c.setBairro(tfBairro.getText());
            c.setRua(tfRua.getText());
            c.setNumero(Integer.parseInt(tfNumero.getText()));
            c.setCpf(jfCpf.getText());
            c.setTelefone(jfTel.getText());
            c.setDtnascimento(jfData.getText());
            if (clienteCRUD.create(c) == true) {
                JOptionPane.showMessageDialog(rootPane, "Inserido com Sucesso");
            } else {
                JOptionPane.showMessageDialog(rootPane, "Falha na Inserção");

            }
        }

        jcUf.setSelectedIndex(0);
        jfCpf.setText("");
        jfData.setText("");
        jfTel.setText("");
        tfBairro.setText("");
        tfNome.setText("");
        tfNumero.setText("");
        tfRua.setText("");
    }//GEN-LAST:event_brSalvarActionPerformed

