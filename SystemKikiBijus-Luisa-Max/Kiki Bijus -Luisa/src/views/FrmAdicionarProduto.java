    private void btCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCadastrarActionPerformed
        // TODO add your handling code here:
        Produto produto = new Produto();

        if (tfNome.getText().equals("") || tfPreco.getText().equals("") || cbFornecedor.getSelectedItem().equals("Escolha") || tfQuantidade.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Favor preencher todos os valores");
        } else {

            Fornecedor f = (Fornecedor) cbFornecedor.getSelectedItem();
            produto.setNome(tfNome.getText());
            produto.setQuantidade(Integer.parseInt(tfQuantidade.getText()));
            produto.setValor(Float.parseFloat(tfPreco.getText().replace(",", ".")));
            produto.setCodigoFornecedor(f.getId());

            if (produtoCRUD.create(produto) == true) {

                JOptionPane.showMessageDialog(rootPane, "Adicionado com sucesso");

            } else {
                JOptionPane.showMessageDialog(rootPane, "Não Adicionado");

            }
        }
        this.tfNome.setText("");
        this.tfPreco.setText("");
        this.tfQuantidade.setText("");
        this.cbFornecedor.setSelectedIndex(0);

    }//GEN-LAST:event_btCadastrarActionPerformed

