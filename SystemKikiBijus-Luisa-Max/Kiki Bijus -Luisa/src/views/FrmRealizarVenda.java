    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        Venda venda = new Venda();

        if (cbClientes.getSelectedItem().equals("Escolha") || cbProdutos.getSelectedItem().equals("Escolha")) {
            JOptionPane.showMessageDialog(rootPane, "Favor preencher todos os valores");
        } else {

            Cliente c = (Cliente) cbClientes.getSelectedItem();
            Produto p = (Produto) cbProdutos.getSelectedItem();
            venda.setCodCliente(c.getId());
            venda.setCodProduto(p.getCodigo());
            venda.setDesconto(Float.parseFloat(tfDesconto.getText().replace(",", ".")));
            venda.setValorVenda(p.getValor());
            
            if (rbPGCartao.isSelected()) {
                venda.setPagamento("C");
            }
            if (rbPGVista.isSelected()) {
                venda.setPagamento("V");
            }

            
            if (vendaCRUD.create(venda) == true) {

                JOptionPane.showMessageDialog(rootPane, "Adicionado com sucesso");

            } else {
                JOptionPane.showMessageDialog(rootPane, "Não Adicionado");

            }
        }
        this.tfDesconto.setText("");
        this.tfValor.setText("");
        this.cbClientes.setSelectedIndex(0);
        this.cbProdutos.setSelectedIndex(0);
    }//GEN-LAST:event_jButton2ActionPerformed

