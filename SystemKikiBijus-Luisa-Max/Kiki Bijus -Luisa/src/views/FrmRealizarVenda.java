    private void listarProdutos() {
        ArrayList<Produto> list = produtoCRUD.read();
        DefaultComboBoxModel modelo = new DefaultComboBoxModel();
        cbProdutos.setModel(modelo);
        cbProdutos.removeAllItems();
        cbProdutos.addItem("Escolha");
        for (Produto lista1 : list) {
            modelo.addElement(lista1);
        }
    }

    private void listarClientes() {
        ArrayList<Cliente> list = clienteCRUD.read();
        DefaultComboBoxModel modelo = new DefaultComboBoxModel();
        cbClientes.setModel(modelo);
        cbClientes.removeAllItems();
        cbClientes.addItem("Escolha");
        for (Cliente lista1 : list) {
            modelo.addElement(lista1);
        }
    }

