    public void preencher() {
        lista = clienteCRUD.read();
        if (modelo.getRowCount() > 0) {
            for (int i = modelo.getRowCount() - 1; i >= 0; i--) {
                modelo.removeRow(i);
            }
        }
        for (Cliente u : lista) {
            modelo.addRow(new Object[]{u.getId(), u.getNome(), u.getCpf(), u.getTelefone()});
        }
    }

