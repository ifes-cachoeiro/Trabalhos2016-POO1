    private void jbAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAlterarActionPerformed
        // TODO add your handling code here:
        Usuario usuario = new Usuario("", "");

        usuario.setNome(tfNome.getText());
        usuario.setEmail(tfEmail.getText());
        usuario.setSenha(pfSenhaN.getText());
        usuario.setId(Integer.parseInt(tfId.getText()));
        if (usuarioCRUD.update(usuario) == true) {
            JOptionPane.showMessageDialog(rootPane, "Alterado com sucesso");
            preencher();
        } else {
            JOptionPane.showMessageDialog(rootPane, "NÃ£o Alterado");

        }
    }//GEN-LAST:event_jbAlterarActionPerformed

    private void preencher() {
        lista = usuarioCRUD.read();
        if (modelo.getRowCount() > 0) {
            for (int i = modelo.getRowCount() - 1; i >= 0; i--) {
                modelo.removeRow(i);
            }
        }

        for (Usuario u : lista) {
            modelo.addRow(new Object[]{u.getId(), u.getNome(), u.getEmail()});
        }
    }
    
