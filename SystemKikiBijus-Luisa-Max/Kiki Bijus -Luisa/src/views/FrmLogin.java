    private void pfSenhaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_pfSenhaKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            Usuario usr;

            usuario.setEmail(tfEmail.getText());
            usuario.setSenha(pfSenha.getText());

            usr = usuarioCRUD.read(usuario);
            if (usr == null) {
                JOptionPane.showMessageDialog(this, "Usuario e senha inválidos!!");
            } else {
                FrmMain principal = new FrmMain();
                principal.setVisible(true);
                principal.receber(usr);
                this.dispose();
            }
        }
    }//GEN-LAST:event_pfSenhaKeyPressed

