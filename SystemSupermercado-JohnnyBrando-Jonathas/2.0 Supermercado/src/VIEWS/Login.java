    private void entrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_entrarActionPerformed
        // TODO add your handling code here:
        int teste = 0;
        for(Usuario u : Database_listas.usuarios){
            
            if ((u.getSenha().equals(senha.getText())) && (u.getEmail().equals(Login.getText()))){
                
                confirma_login c = new confirma_login();
                c.setVisible(true);
                this.dispose();
                teste = 1;
            }
            
        }
        
        if (teste == 0) {
            
            erro_login e = new erro_login();
            e.setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_entrarActionPerformed

