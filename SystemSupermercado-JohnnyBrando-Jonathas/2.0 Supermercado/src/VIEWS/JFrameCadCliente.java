    private void ConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConfirmarActionPerformed
        // TODO add your handling code here:
        boolean radio;
        radio = pf.isSelected();
        if (radio){
            ///crio pessoa fisica
            PessoaFisica p = new PessoaFisica(txNOME.getText(), txCIDADE.getText(), txBAIRRO.getText(), txUF.getText(), txNUMERO.getText(), txTELEFONE.getText(), txCPF.getText());
            ClienteCRUD.inserirCliente(p);
        } else {
            ///crio pessoa juridica
            PessoaJuridica p = new PessoaJuridica(txNOME.getText(), txCIDADE.getText(), txBAIRRO.getText(), txUF.getText(), txNUMERO.getText(), txTELEFONE.getText(), txCNPJ.getText());
            ClienteCRUD.inserirCliente(p);
        }
        
        JFrameConfirmar c = new JFrameConfirmar();
        c.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_ConfirmarActionPerformed

