    private void ConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConfirmarActionPerformed
        // TODO add your handling code here:
        
        int ALTERAR = 0;
        int codAux = 0;
        int teste = 0;
        try{
            codAux = Integer.parseInt(cod.getText());
        } catch(java.lang.NumberFormatException e){
            teste = 1;
            this.dispose();
            erro_formato3 erro = new erro_formato3();
            erro.setVisible(true);
            
        }
        
        if (teste == 0) {
            boolean radio;
        radio = pf.isSelected();
        ALTERAR_CLIENTE_CONFIRMA ffc;
        
        
        
        for (Cliente c : Database_listas.clientesSalvos) {
            if (c.getCod_cliente() == codAux) {
                if (radio){
                    
                    PessoaFisica p;
                    p = (PessoaFisica) c;
                    ffc = new ALTERAR_CLIENTE_CONFIRMA(p);
                } else {
                    
                    PessoaJuridica p;
                    p = (PessoaJuridica) c;
                    ffc = new ALTERAR_CLIENTE_CONFIRMA(p);
                }                                
                ffc.setVisible(true);    
                this.dispose();
                ALTERAR = 1;
                break;
           }
            }
            if (ALTERAR == 0){
                this.dispose();
                erro_menu_principal e = new erro_menu_principal();
                e.setVisible(true);
            }
        } else {
            erro_formato3 erro = new erro_formato3();
            erro.setVisible(true);
            this.dispose();
        }
        
    }//GEN-LAST:event_ConfirmarActionPerformed

