    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        this.dispose();
        int excluido = 0;
        int teste = 0;
        int codAux = 0;
        try {
             codAux = Integer.parseInt(jTextField2.getText());
        } catch (java.lang.NumberFormatException e) {
            teste = 1;
        }
       
        if (teste == 0) {
            for (Funcionario fu : Database_listas.funcionariosSalvos) {
            if (fu.getID_funcionario()== codAux) {
                JFrameExcluirFuncionarioConfirma fuc = new JFrameExcluirFuncionarioConfirma(fu);
                fuc.setVisible(true);
                excluido = 1;
                break;
           }
            }
            if (excluido == 0){
                this.dispose();
                TELA_CRUD_FUNCIONARIO f = new TELA_CRUD_FUNCIONARIO();
                f.setVisible(true);
            }
        } else {
            erro_formato3 erro = new erro_formato3();
            erro.setVisible(true);
            this.dispose();
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

