        this.dispose();
        int excluido = 0;
        int teste = 0;
        int codAux = 0;
        try {
            codAux = Integer.parseInt(jTextField1.getText());
        } catch (java.lang.NumberFormatException e) {
            teste = 1;
        }
        
        if (teste == 0) {
           for (Cliente c : Database_listas.clientesSalvos) {
            if (c.getCod_cliente()== codAux) {
                JFrameExcluirClienteConfirma cli = new JFrameExcluirClienteConfirma(c);
                cli.setVisible(true);
                excluido = 1;
                break;
           }
            }
            if (excluido == 0){
                this.dispose();
                TELA_CRUD_FUNCIONARIO f = new TELA_CRUD_FUNCIONARIO();
                f.setVisible(true);
            } 
        } else {
            erro_formato3 erro = new erro_formato3();
            erro.setVisible(true);
            this.dispose();
        }
        
