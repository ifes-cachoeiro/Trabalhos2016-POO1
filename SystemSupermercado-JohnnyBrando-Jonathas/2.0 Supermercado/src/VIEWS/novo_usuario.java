    private void ConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConfirmarActionPerformed
        // TODO add your handling code here:
        int teste1 = 0, teste2 = 0, teste3 = 0, teste4 = 0;
        int cont = 0;
        char [] letras = senha.getText().toCharArray();
        
        if (senha.getText().length() >= 6){
           
            while(cont < senha.getText().length()){
                if (((letras[cont]  > 64) && (letras[cont] < 91)) || (letras[cont]  > 96) && (letras[cont] < 123)){
                    teste1 = 1;
                } else if ((letras[cont] > 47) && ( letras[cont] < 58)){
                    teste2 = 1;
                } else if ((letras[cont] > 32) && ( letras[cont] < 48) || ((letras[cont] > 57 ) && (letras[cont] < 65) )){
                   teste3 = 1;        
                }

                cont++;
            }
            
        }
        
        cont = 0;
        char [] letras2 = email.getText().toCharArray();
        
        while(cont < email.getText().length()){
            
            if (letras2[cont] == 64){
                teste4 = 1;
            }
            cont++;
        }
        
       
       if ( (teste1 == 0) || (teste2 == 0) || (teste3 == 0)){
           try {
                throw new formato_senha();
            } catch (formato_senha ex) {
                erro_formato2 e = new erro_formato2();
                e.setVisible(true);
                this.dispose();
            }
       }  else if (teste4 == 0){
                try {
                     throw new formato_email();
                 } catch (formato_email ex) {
                     erro_formato e = new erro_formato();
                     e.setVisible(true);
                     this.dispose();
                 }
        } else {
           
           Usuario u = new Usuario(senha.getText(), email.getText(), nome.getText());
           UsuarioCRUD.adicionarUsuario(u);
           add_login a = new add_login();
           a.setVisible(true);
           this.dispose();
           
       }
        
      
           
       
        
        
        
    }//GEN-LAST:event_ConfirmarActionPerformed

