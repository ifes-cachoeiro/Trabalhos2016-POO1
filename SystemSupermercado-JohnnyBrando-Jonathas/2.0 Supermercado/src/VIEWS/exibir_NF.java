    private void confirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmarActionPerformed
        // TODO add your handling code here:
        int teste = 0;
        int teste2 = 0;
        try {
            int codigo2 = Integer.parseInt(codigo.getText());
        } catch (java.lang.NumberFormatException e) {
            teste2 = 1;
        }
        
        if (teste2 == 0) {
            for (Nota_Fiscal n : Database_listas.NFSalvas){
                if (Integer.parseInt(codigo.getText()) == n.getId_local()){
                    for (Cliente c : Database_listas.clientesSalvos){
                        if (c.getCod_cliente() == n.getId_cliente()) {
                            cliente.setText(c.getNome());
                        }
                    }
                    for (Funcionario f : Database_listas.funcionariosSalvos){
                        if (f.getID_funcionario() == n.getId_funcionario()){
                            funcionario.setText(f.getNome_completo());
                        }
                    }
                    data.setText(n.getData_emissao());
                    valor.setText(String.valueOf(n.getValor_total()));
                    DefaultTableModel tabela = (DefaultTableModel) tabela_produto.getModel();
                    tabela.setNumRows(0);
                    for(Produto po : n.getProdutos()){
                        tabela.addRow(po.obterDados2(po));
                    } 
                }
            }
        } else {
            erro_formato3 erro = new erro_formato3();
            erro.setVisible(true);
            this.dispose();

        }
        
        
        
    }//GEN-LAST:event_confirmarActionPerformed

